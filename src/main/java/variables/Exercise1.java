package variables;

public class Exercise1 {

    public static void main(String[] args) {

        byte byteNumber = 30;
        System.out.println("Wartosc zmiennej byteNumber to: " + byteNumber);

        short shortNumber = 32000;
        System.out.println("Wartosc zmiennej shortNumber to: " + shortNumber);

        int intNumber = 2_000_000;
        System.out.println("Wartosc zmiennej intNumber to: " + intNumber);

        long longNumber = 200L;
        System.out.println("Wartosc zmiennej longNumber to: " + longNumber);

        float floatNumber = 5.0f;
        System.out.println("Wartosc zmiennej floatNumber to: " + floatNumber);

        double doubleNumber = 15.0d;
        System.out.println("Wartosc zmiennej doubleNumber to: " + doubleNumber);

        boolean logicVar = true;
        System.out.println("Wartosc zmiennej logicVar to: " + logicVar);

        char c = 'M';
        System.out.println("Wartosc zmiennej c to: " + c);

        String word = "Ala ma kota.";
        System.out.println("Wartosc zmiennej word to: " + word);


    }

}
