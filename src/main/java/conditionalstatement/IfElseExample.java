package conditionalstatement;

public class IfElseExample {

    public static void main(String[] args) {

        int number = 3;

        if (number == 4) {
            System.out.println("Liczba ma wartość 4");
        } else if (number > 4) {
            System.out.println("Liczba jest większa niż 4");
        } else {
            System.out.println("Liczba jest mniejsza niż 4");
        }

    }

}
